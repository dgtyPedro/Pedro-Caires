<h3>React + Typescript is (kinda) hard </h3>

<p>
in the same way that saying that something is easy in programming, 
saying that something is difficult is bullshit. Everything can be learned, 
studied and applied however for someone who doesn't have Javascript so ingrained in their veins the first 
time an npm/yarn build returns 82 type errors can be extremely frightening, here we say more specifically of 
Typescript than React, React can get quite complex with the addition of hooks, contexts and useEffects in projects 
but Typescript is different, the type of difficulty is different.
</p>

<h4>Typescript do not forgive</h4>
<p>
The worst thing you can do is hand Typescript to a 
developer who doesn't understand its usefulness, Typescript is much more about prevention than solution, 
a developer (especially Junior ones) likes to see results, things popping up on their screen , 
Typescript doesn't do that, in fact what it can do is show you 300 different errors in its IDE of codes 
that are supposed to be right, this creates a frustration in the developer's head that makes him hate the 
"semi-language" . Typescript does not allow inconsistencies and for the novice developer who is an expert at 
creating inconsistent code this can be a problem.
</p>

<h3>React is much more mature than Vue</h3>
<p>Vue is great and I've already written a whole text here saying why however React 
    has a power and independence that is hard to trade, I do think it's hard to do something 
    that React does and Vue doesn't, but when you develop you just feel like everything in React is more 
    mature, the patterns are more sophisticated, the support list is more extensive, and the range of libraries is 
    just better.</p>


